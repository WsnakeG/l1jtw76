package com.lineage.server.templates;

public class L1Scenery {

	private int _gfxid;

	private int _locx;

	private int _locy;

	private int _heading;

	private int _mapid;

	private String _html;

	/**
	 * @return 傳出 _gfxid
	 */
	public int get_gfxid() {
		return _gfxid;
	}

	/**
	 * @param gfxid 對 _gfxid 進行設置
	 */
	public void set_gfxid(final int gfxid) {
		_gfxid = gfxid;
	}

	/**
	 * @return 傳出 _locx
	 */
	public int get_locx() {
		return _locx;
	}

	/**
	 * @param locx 對 _locx 進行設置
	 */
	public void set_locx(final int locx) {
		_locx = locx;
	}

	/**
	 * @return 傳出 _locy
	 */
	public int get_locy() {
		return _locy;
	}

	/**
	 * @param locy 對 _locy 進行設置
	 */
	public void set_locy(final int locy) {
		_locy = locy;
	}

	/**
	 * @return 傳出 _heading
	 */
	public int get_heading() {
		return _heading;
	}

	/**
	 * @param heading 對 _heading 進行設置
	 */
	public void set_heading(final int heading) {
		_heading = heading;
	}

	/**
	 * @return 傳出 _mapid
	 */
	public int get_mapid() {
		return _mapid;
	}

	/**
	 * @param mapid 對 _mapid 進行設置
	 */
	public void set_mapid(final int mapid) {
		_mapid = mapid;
	}

	/**
	 * @return 傳出 _html
	 */
	public String get_html() {
		return _html;
	}

	/**
	 * @param html 對 _html 進行設置
	 */
	public void set_html(final String html) {
		_html = html;
	}

}
